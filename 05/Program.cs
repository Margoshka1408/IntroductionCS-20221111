int a=Convert.ToInt32(Console.ReadLine());
int b=Convert.ToInt32(Console.ReadLine());
int c=Convert.ToInt32(Console.ReadLine());
if (a>b && a>c) System.Console.WriteLine($"{a} является максимальное");
if (b>a && b>c) System.Console.WriteLine($"{b} является максимальное");
if (c>a && c>b) System.Console.WriteLine($"{c} является максимальное");























